fn main(){
    // 1. Integer
    // i32: Standart integer tpye used in Rust
    let days: i32 = 7;

    // 2. Float 
    // f64: Strandart decimal number type
    let completion_rate: f64 = 45.5;

    // 3. Boolen
    // true or false logic
    let is_active: bool = true;

    // 4. Character
    // Single quotes are used for chars ' ' 
    let grade: char = 'A';

    // Print the variables
    println!("Days to learn: {}", days);
    println!("Completion Rate: {}", completion_rate);
    println!("Is Active User: {}", is_active);
    println!("Course Grade: {}", grade);
}
